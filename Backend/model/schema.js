import mongoose from "mongoose";
import Joi from 'joi'


const signupSchema = mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  tel: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  confirmPassword: {
    type: String,
    required: true,
  }
});

const Signup = mongoose.model('Signup' , signupSchema)
export default Signup